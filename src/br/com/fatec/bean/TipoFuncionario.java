package br.com.fatec.bean;

/**
 *
 * @author ProfAlexandre
 */
public class TipoFuncionario {
    
    private int id;
    private String desc;

    public TipoFuncionario(int id, String desc) {
        this.id = id;
        this.desc = desc;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    @Override
    public String toString() {
        return "TipoFuncionario{" + "id=" + id + ", desc=" + desc + '}';
    }
    
}
